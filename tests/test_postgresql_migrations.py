from __future__ import annotations

import re
from pathlib import Path


BASELINE = Path("migrations/versions/20260608_0001_postgresql_baseline.py")


def _baseline_text() -> str:
    return BASELINE.read_text(encoding="utf-8")


def test_postgresql_baseline_uses_namespaces_for_storage_domains() -> None:
    text = _baseline_text()

    assert "CREATE SCHEMA IF NOT EXISTS wolf" in text
    assert "CREATE SCHEMA IF NOT EXISTS registry" in text
    assert "CREATE SCHEMA IF NOT EXISTS evolution" in text
    assert "CREATE TABLE registry.rejected_proposals" in text
    assert "CREATE TABLE evolution.rejected_proposals" in text
    assert "CREATE TABLE rejected_proposals" not in text


